# интерпретатор выделяет область памяти, сохраняет в неё объект данных 2025, ссылку на эту область памяти ассоциирует с идентификатором var
var = 2025

# любой идентификатор может быть связан с любым объектом данных любого типа
var = 'hello'
# в данном случае доступ к объекту данных 2025 будет потерян!


# = это оператор присваивания, является бинарным оператором
# <левый_операнд> = <правый_операнд>

